    private class RemoveGuestFromRoomSwingWorker extends SwingWorker<Void, Void> {

        @Override
        protected Void doInBackground() throws Exception {
            if (jTable6.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(allAccomPanel, myBundle.getString("warning3"));
                return null;
            }
            int row = jTable6.getSelectedRow();
            Long accomID = (Long) jTable6.getValueAt(row, 0);
            Accomodation accom = accomManager.getAccom(accomID);
            AllAccomTableModel model = (AllAccomTableModel) jTable6.getModel();
            try {
                accomManager.deleteAccom(accom);
                accom.setAccomId(accomID);
                model.deleteAccom(accom);
                model = (AllAccomTableModel) jTable5.getModel();
                model.deleteAccom(accom);
                accom.setAccomId(accomID);
                ((AllAccomTableModel) jTable5.getModel()).deleteAccom(accom);
                ((AllAccomTableModel) jTable6.getModel()).deleteAccom(accom);
                ((AllAccomComboBoxModel) jComboBox14.getModel()).deleteAccom(accom);
            } catch (Exception ex) {
                LOG.log(Level.SEVERE, ex.getMessage());
            }
            LOG.log(Level.INFO, "Guest({0}) successfully removed from room({1})", new Object[]{accom.getGuestId(), accom.getRoomId()});
            return null;
        }
    }
