    private DeleteGuestSwingWorker deleteGuestSwingWorker;

    private class DeleteGuestSwingWorker extends SwingWorker<Void, Void> {

        @Override
        protected Void doInBackground() throws Exception {
            if (jComboBox13.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(allAccomPanel, myBundle.getString("warning1"));
                return null;
            }
            Long guestID = Long.valueOf(((String) jComboBox13.getSelectedItem()).split(" ")[0]);

            boolean isSafeToDelete = true;    // you cannot delete guest used in accom
            List<Accomodation> accoms = accomManager.getAllAccoms();
            for (Accomodation accom : accoms) {
                if (Objects.equals(accom.getGuestId(), guestID)) {
                    isSafeToDelete = false;
                }
            }

            if (isSafeToDelete) {
                try {
                    Guest newGuest = guestManager.getGuest(guestID);
                    guestManager.deleteGuest(newGuest);
                    newGuest.setGuestID(guestID);
                    ((AllGuestComboBoxModel) jComboBox12.getModel()).deleteGuest(newGuest);
                    ((AllGuestComboBoxModel) jComboBox13.getModel()).deleteGuest(newGuest);
                    ((AllGuestComboBoxModel) jComboBox6.getModel()).deleteGuest(newGuest);
                    ((AllGuestComboBoxModel) jComboBox1.getModel()).deleteGuest(newGuest);
                    ((AllGuestComboBoxModel) jComboBox15.getModel()).deleteGuest(newGuest);
                    ((AllGuestsTableModel) jTable3.getModel()).deleteGuest(newGuest);
                } catch (Exception ex) {
                    LOG.log(Level.SEVERE, ex.getMessage());
                }
                LOG.log(Level.INFO, "Deleted guest ({0}).", guestID);
            } else {
                JOptionPane.showMessageDialog(allAccomPanel, myBundle.getString("warning7"));
            }
            return null;
        }
    }
