    private AccomodateSwingWorker accomodateSwingWorker;

    private class AccomodateSwingWorker extends SwingWorker<Void, Void> {

        @Override
        protected Void doInBackground() throws Exception {
            if (jComboBox1.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(allAccomPanel, myBundle.getString("warning1"));
                return null;
            }
            if (jComboBox2.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(allAccomPanel, myBundle.getString("warning2"));
                return null;
            }
            Guest guest;

            Long guestID = (Long.valueOf(((String) (jComboBox1.getSelectedItem())).split(" ")[0]));
            guest = guestManager.getGuest(guestID);
            Room room = roomManager.getRoom((Long) jComboBox2.getSelectedItem());
            Date startDate = date(startYearCombo.getSelectedItem() + "-"
                    + (startMonthCombo.getSelectedIndex() + 1) + "-" + startDayCombo.getSelectedItem());
            Date endDate = date(endYearCombo.getSelectedItem() + "-"
                    + (endMonthCombo.getSelectedIndex() + 1) + "-" + endDayCombo.getSelectedItem());
            if (startDate.compareTo(endDate) > 0) {
                JOptionPane.showMessageDialog(allAccomPanel, myBundle.getString("warning9"));
                return null;
            }
            Accomodation accom = new Accomodation();
            accom.setGuestId(guest.getGuestID());
            accom.setRoomId(room.getRoomID());
            accom.setStartDate(startDate);
            accom.setEndDate(endDate);
            int used_cap = hotelmanager.findGuestsfromRoom(room, startDate).size();
            if (used_cap >= room.getCapacity()) {
                LOG.log(Level.SEVERE, "No more space in room with ID {0}", room.getRoomID());
                return null;
            }
            try {
                accomManager.createAccom(accom);
                ((AllAccomTableModel) jTable5.getModel()).addAccom(accom);
                ((AllAccomTableModel) jTable6.getModel()).addAccom(accom);
                ((AllAccomComboBoxModel) jComboBox14.getModel()).addAccom(accom);
            } catch (Exception ex) {
                LOG.log(Level.SEVERE, ex.getMessage());
                return null;
            }
            LOG.log(Level.INFO, "Guest({0}) accomodated in room({1})", new Object[]{guest.getGuestID(), room.getRoomID()});
            return null;
        }
    }
