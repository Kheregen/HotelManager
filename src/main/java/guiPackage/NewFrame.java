    private DeleteRoomSwingWorker deleteRoomSwingWorker;

    private class DeleteRoomSwingWorker extends SwingWorker<Void, Void> {

        @Override
        protected Void doInBackground() throws Exception {
            if (jComboBox11.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(allAccomPanel, myBundle.getString("warning2"));
                return null;
            }

            Long roomID = (Long) jComboBox11.getSelectedItem();
            boolean isSafeToDelete = true;    // you cannot delete room used in accom
            List<Accomodation> accoms = accomManager.getAllAccoms();
            for (Accomodation accom : accoms) {
                if (Objects.equals(accom.getRoomId(), roomID)) {
                    isSafeToDelete = false;
                }
            }

            if (isSafeToDelete) {
                try {
                    Room newRoom = roomManager.getRoom(roomID);
                    roomManager.deleteRoom(newRoom);
                    newRoom.setRoomID(roomID);
                    ((AllRoomsTableModel) jTable4.getModel()).deleteRoom(newRoom);
                    ((AllRoomsComboBoxModel) jComboBox10.getModel()).deleteRoom(newRoom);
                    ((AllRoomsComboBoxModel) jComboBox11.getModel()).deleteRoom(newRoom);
                    ((AllRoomsComboBoxModel) jComboBox2.getModel()).deleteRoom(newRoom);
                    ((AllRoomsComboBoxModel) jComboBox16.getModel()).deleteRoom(newRoom);
                    ((AllRoomsComboBoxModel) findGuestsPanelCombo.getModel()).deleteRoom(newRoom);
                } catch (Exception ex) {
                    LOG.log(Level.SEVERE, ex.getMessage());
                }
                LOG.log(Level.INFO, "Deleted room ({0}).", roomID);
            } else {
                JOptionPane.showMessageDialog(allAccomPanel, myBundle.getString("warning6"));
            }
            return null;
        }
    }
